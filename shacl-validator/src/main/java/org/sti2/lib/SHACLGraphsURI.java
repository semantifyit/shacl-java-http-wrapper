package org.sti2.lib;

/**
 * SHACLGraphsURI
 */
public class SHACLGraphsURI {

    String dataGraphUri;
    String shapeGraphUri;
    
    /**
     * @return the dataGraphUri
     */
    public String getDataGraphUri() {
        return dataGraphUri;
    }

    /**
     * @param dataGraphUri the dataGraphUri to set
     */
    public void setDataGraphUri(String dataGraphUri) {
        this.dataGraphUri = dataGraphUri;
    }

    /**
     * @return the shapeGraphUri
     */
    public String getShapeGraphUri() {
        return shapeGraphUri;
    }
    
    /**
     * @param shapeGraphUri the shapeGraphUri to set
     */
    public void setShapeGraphUri(String shapeGraphUri) {
        this.shapeGraphUri = shapeGraphUri;
    }
}