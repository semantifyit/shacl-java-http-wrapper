package org.sti2.lib;

/**
 * SHACLGraphs
 */
public class SHACLGraphs {

    String dataGraph;
    String shapeGraph;

    /**
     * @return the dataGraph
     */
    public String getDataGraph() {
        return dataGraph;
    }

    /**
     * @param dataGraph the dataGraph to set
     */
    public void setDataGraph(String dataGraph) {
        this.dataGraph = dataGraph;
    }

    /**
     * @return the shapeGraph
     */
    public String getShapeGraph() {
        return shapeGraph;
    }

    /**
     * @param shapeGraph the shapeGraph to set
     */
    public void setShapeGraph(String shapeGraph) {
        this.shapeGraph = shapeGraph;
    }
}